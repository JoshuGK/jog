import math


def get_sine(x):
    angle_rad=math.sin(x)
    angle_sin=math.sin(angle_rad)
    return angle_sin
def get_tan(x):
    angle_rad=math.tan(x)
    angle_tan=math.tan(angle_rad)
    return angle_tan
def get_cosin(x):
    angle_rad=math.cos(x)
    angle_cos=math.cos(angle_rad)
    return angle_cos